private void displayReviewsData() {
        try {
            // Read data from the reviews.txt file
            BufferedReader reader = new BufferedReader(new FileReader("reviews.txt"));
            StringBuilder reviewsData = new StringBuilder();

            String line;
            while ((line = reader.readLine()) != null) {
                reviewsData.append(line).append("\n");
            }

            // Set the reviews data in the text area
            jTextArea1.setText(reviewsData.toString());

            // Close the file reader
            reader.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

