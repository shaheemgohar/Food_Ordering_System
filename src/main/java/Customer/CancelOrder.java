    private void CancelOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelOrderActionPerformed
     // Get the entered order ID
        String orderId = jTextPane2.getText();

        // Validate if the entered order ID is not empty
        if (orderId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Order ID.", "Cancel Order Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Remove the order based on the entered Order ID
        if (removeOrder(orderId)) {
            // Display success message if the order is successfully removed
            JOptionPane.showMessageDialog(this, "Order canceled successfully!", "Cancel Order Success", JOptionPane.INFORMATION_MESSAGE);

            // Refresh the displayed order data
            displayOrderData();
        } else {
            // Display an error message if order cancellation fails
            JOptionPane.showMessageDialog(this, "Failed to cancel the order. Please try again.", "Cancel Order Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_CancelOrderActionPerformed

