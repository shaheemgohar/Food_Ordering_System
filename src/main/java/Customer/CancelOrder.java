private void displayOrderData() {
        try {
            // Read data from the orders.txt file
            BufferedReader reader = new BufferedReader(new FileReader("orders.txt"));
            StringBuilder orderData = new StringBuilder();

            String line;
            while ((line = reader.readLine()) != null) {
                orderData.append(line).append("\n");
            }

            // Set the order data in the text area
            jTextArea1.setText(orderData.toString());

            // Close the file reader
            reader.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
