private boolean removeOrder(String orderId) {
        try {
            // Read all orders from the file
            BufferedReader reader = new BufferedReader(new FileReader("orders.txt"));
            StringBuilder orders = new StringBuilder();
            String line;

            while ((line = reader.readLine()) != null) {
                // Check if the order ID matches the one to be removed
                if (!line.startsWith(orderId)) {
                    orders.append(line).append("\n");
                }
            }

            // Close the file reader
            reader.close();

            // Write the updated orders (excluding the one to be removed) back to the file
            BufferedWriter writer = new BufferedWriter(new FileWriter("orders.txt"));
            writer.write(orders.toString());

            // Close the file writer
            writer.close();

            return true; // Return true to indicate successful order removal
        } catch (IOException e) {
            e.printStackTrace();
            return false; // Return false if order removal fails
        }
    }
