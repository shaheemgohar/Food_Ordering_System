private void displayMenuData() {
        try {
            // Read data from the menu.txt file
            BufferedReader reader = new BufferedReader(new FileReader("menu.txt"));
            StringBuilder menuData = new StringBuilder();

            String line;
            while ((line = reader.readLine()) != null) {
                menuData.append(line).append("\n");
            }

            // Set the menu data in the text area
            jTextArea1.setText(menuData.toString());

            // Close the file reader
            reader.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
