    private void PlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PlaceOrderActionPerformed
           // Get the entered item ID and quantity
        String itemId = jTextPane1.getText();
        String quantityStr = jTextPane2.getText();

        // Validate if the entered values are not empty
        if (itemId.isEmpty() || quantityStr.isEmpty()) {
            System.out.println("Please enter both item ID and quantity.");
            return;
        }

        // Parse quantity as an integer
        try {
            int quantity = Integer.parseInt(quantityStr);

            // Place the order
            placeOrder(itemId, quantity);
        } catch (NumberFormatException e) {
            System.out.println("Invalid quantity. Please enter a valid number.");
        }
    }//GEN-LAST:event_PlaceOrderActionPerformed

