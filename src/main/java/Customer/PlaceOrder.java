private static void placeOrder(String itemId, int quantity) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("orders.txt", true))) {
            // Assuming format: OrderID,ItemID,Quantity
            String orderDetails = generateOrderId() + "," + itemId + "," + quantity;
            writer.write(orderDetails);
            writer.newLine();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
private static String generateOrderId() {
        // Implement your logic to generate a unique order ID
        // For simplicity, you can use a timestamp or any other unique identifier
        return "OID" + System.currentTimeMillis();
    }
